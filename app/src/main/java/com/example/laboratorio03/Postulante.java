package com.example.laboratorio03;

public class Postulante {
    String dni;
    String nombres;
    String apellidos;
    String fecha;
    String colegio;
    String carrera;


    public Postulante(String _dni, String _nombres, String _apellidos, String _fecha, String _colegio, String _carrera  ){
        dni = _dni;
        nombres = _nombres;
        apellidos = _apellidos;
        fecha = _fecha;
        colegio = _colegio;
        carrera = _carrera;
    }
}
